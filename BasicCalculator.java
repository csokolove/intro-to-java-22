class BasicCalculator {
    public static void main(String[] args) {
        int add1 = 4;
        double add2 = 8.7;

        int sub1 = 10;
        double sub2 = 5.2;

        int mult1 = 12;
        double mult2 = 20.4;

        int div1 = 16;
        int div2 = 4;

        System.out.println(add1 + add2);
        System.out.println(sub1 - sub2);
        System.out.println(mult1 * mult2);
        System.out.println(div1 / div2);
        System.out.println(div1 % div2);
    }
}
